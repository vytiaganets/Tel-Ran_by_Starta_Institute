public class Unary {
    public static void main(String[] args) {
        int num = 5;
        int numMinus = -num;
        int num1 = -20;
        int num1Pos = -num1;
        System.out.println(num);//5
        System.out.println(numMinus);//-5
        System.out.println(num1);//-20
        System.out.println(num1Pos);//20
    }
}
